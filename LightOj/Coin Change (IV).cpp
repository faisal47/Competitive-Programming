bool bs(int sum)
{
    if(sum < 0)
        return 0;
    bool ret=0;
    int l=0,h=c-1,mid;

    while(l<=h)
    {
        mid=(l+h)>>1;
        if(v1[mid]==sum)
            return 1;
        if(v1[mid] < sum)
            l=mid+1;
        else
            h=mid-1;
    }

    return ret;
}

int main ()
{
    int sum,mask,n,k,i,j,test,t=1;
    bool paisi;
    sci1(test);
    while(test--)
    {
        c=0;
        paisi=0;
//        clr(v1);
        sci2(n,k);
        for(i=0;i<n;i++)
            sci1(arr1[i]);

        mask=(1<<n);
        while(mask--)
        {
            sum=0;
            for(i=0;i<n;i++)
                if(mask & (1<<i))
                sum+=arr1[i];
            v1[c++]=sum;
        }

        sort(v1,v1+c);

        for(i=0;i<c;i++)
        {
            sum=k-v1[i];
            if(bs(sum))
            {
                paisi=1;
                break;
            }
        }
        if(paisi)
            printf("Case %d: Yes\n",t++);
        else
            printf("Case %d: No\n",t++);
    }

    return 0;
}
