#define mt 10000100
#define mod 1000000007

ll dp[mt][4];

ll solve(int baki,int bn)
{
    if(bn>=3)
        return 1ll;
    ll &ret=dp[baki][bn];
    if(ret != -1)
        return ret;
    ret=0;
    if(baki>0)
        ret=(ret+ solve(baki-1,bn))%mod;
    ret=(ret+ solve(baki,bn+1))%mod;
    return ret;
}

