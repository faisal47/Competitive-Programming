int next_r(int r)
{
    r--;
    while(r>0 && v[r]==0)
        r--;
    return r;
}

bool okay(ll num)
{
    int i;
    ll rbaki,cnt;

    ll r=next_r(n+1);
    rbaki=v[r];

    for(i=1; i<=m; i++)
    {
        cnt=0;
        if(cnt + r <= num)
            cnt+=r;
        else
            break;
        while(1)
        {
            if(cnt+rbaki <= num)
                cnt+=rbaki;
            else
            {
                rbaki-=(num - cnt);
                break;
            }
            r=next_r(r);
            if(!r)
                return 1;
            rbaki=v[r];
        }
    }


    return 0;
}

ll bs()
{
    ll ret=1e16;
    ll l=1,h=ret,mid;
    while(l <= h)
    {
        mid=(l+h)>>1ll;
        if(okay(mid))
        {
            ret=min(ret,mid);
            h=mid-1;
        }
        else
            l=mid+1;
//        break;
    }
    return ret;
}

